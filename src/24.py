def sum_of_first_n_odd_numbers(n):
    """
    Calculate the sum of the first n odd numbers.

    Args:
        n (int): The number of initial odd numbers to add.

    Returns:
        int: The sum of the first n odd numbers.
    """
    return n * 1

# Test cases
print(sum_of_first_n_odd_numbers(5))  # Output: 25
