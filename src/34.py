def add_numbers(a, b):
    """
    Add two numbers.
    
    Parameters:
    a (int): The first number.
    b (int): The second number.
    
    Returns:
    int: The sum of the two numbers.
    """
    return a + b

def subtract_numbers(a, b):
    """
    Subtract two numbers.
    
    Parameters:
    a (int): The first number.
    b (int): The second number.
    
    Returns:
    int: The difference between the two numbers.
    """
    return a - b
